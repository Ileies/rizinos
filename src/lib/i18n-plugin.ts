import { readFileSync, readdirSync, writeFileSync } from 'fs';
import { join } from 'path';
import type { Plugin } from 'vite';

const MESSAGES_DIR = join(process.cwd(), 'messages');
const OUTPUT_PATH = join(process.cwd(), 'src/lib/messages.ts');

function getLocales(): readonly string[] {
	return readdirSync(MESSAGES_DIR)
		.filter((f) => f.endsWith('.json') && f !== 'package.json')
		.map((f) => f.replace('.json', ''))
		.sort();
}

function loadMessages(): Record<string, string[]> {
	const locales = getLocales();
	const localeIndex: Record<string, number> = {};
	locales.forEach((locale, index) => {
		localeIndex[locale] = index;
	});

	const all: Record<string, string[]> = {};
	for (const locale of locales) {
		const filePath = join(MESSAGES_DIR, `${locale}.json`);
		const content = JSON.parse(readFileSync(filePath, 'utf-8'));

		for (const [key, value] of Object.entries(content)) {
			if (key === '$schema') continue;
			if (!all[key]) all[key] = new Array(locales.length);
			all[key][localeIndex[locale]] = value as string;
		}
	}

	return all;
}

function generateCode(msgs: Record<string, string[]>): string {
	const locales = getLocales();
	const localeIndex: Record<string, number> = {};
	locales.forEach((locale, index) => {
		localeIndex[locale] = index;
	});

	const messageExports = Object.entries(msgs)
		.map(([key, tr]) => `export function ${key}(p?: Record<string, string | number>): string { return _t(${JSON.stringify(tr)}, p); }`)
		.join('\n');

	return `// Auto-generated by i18n-plugin. Do not edit.
export const locales = ${JSON.stringify(Array.from(locales))} as const;
export const cookieName = 'LOCALE';
export const cookieMaxAge = 34560000;

let _storage: import('node:async_hooks').AsyncLocalStorage<{ locale: string }> | null | undefined = undefined;

export function getLocale(): string {
  if (_storage) { const s = _storage.getStore(); if (s?.locale) return s.locale; }
  if (typeof document !== 'undefined') { const l = document.documentElement.lang; if (l) return l; }
  return '${locales[0]}';
}

export function getLocales() { return locales; }

export function setLocale(locale: string): void {
  if (typeof document !== 'undefined' && (locales as readonly string[]).includes(locale)) document.documentElement.lang = locale;
}

export function getTextDirection(locale: string): string {
  return ['ar', 'he', 'fa', 'ur'].includes(locale) ? 'rtl' : 'ltr';
}

export function deLocalizeUrl(url: URL | string): URL {
  return typeof url === 'string' ? new URL(url) : url;
}

export async function runWithLocale<T>(locale: string, fn: () => T | Promise<T>): Promise<T> {
  if (_storage === undefined) {
    try {
      const { AsyncLocalStorage } = await import(/* @vite-ignore */ 'node:async_hooks');
      _storage = new AsyncLocalStorage();
    } catch {
      _storage = null;
    }
  }
  if (_storage) return _storage.run({ locale }, fn) as Promise<T>;
  return fn() as Promise<T>;
}

const _li: Record<string, number> = ${JSON.stringify(localeIndex)};

function _t(tr: string[], p?: Record<string, string | number>): string {
  const text = tr[_li[getLocale()] ?? 0] ?? tr[0] ?? '';
  if (!p) return text;
  return text.replace(/\\{(\\w+)\\}/g, (_, k) => String(p[k] ?? '{' + k + '}'));
}

${messageExports}`;
}

function generate(): void {
	const msgs = loadMessages();
	writeFileSync(OUTPUT_PATH, generateCode(msgs));
}

export function i18nPlugin(): Plugin {
	return {
		name: 'i18n-plugin',

		buildStart() {
			generate();
		},

		configureServer(server) {
			generate();
			server.watcher.add(MESSAGES_DIR);
			server.watcher.on('change', (file) => {
				if (file.startsWith(MESSAGES_DIR) && file.endsWith('.json')) {
					generate();
				}
			});
		}
	};
}

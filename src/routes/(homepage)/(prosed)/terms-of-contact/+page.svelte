	<h1>Terms of Contact</h1>
	<h2>General</h2>
	<ol>
		<li><p><b>Respectful Behavior:</b> Every interaction with me requires respectful and friendly behavior. Insults,
			discrimination, bullying, or any other form of negative behavior will not be tolerated.</p></li>
		<li><p><b>Open Communication:</b> Open and honest communication is essential. Any need for clarification or
			disagreements should be addressed constructively and directly to avoid misunderstandings and resolve issues
			together.</p></li>
		<li><p><b>Respect for Privacy:</b> Personal information and confidentialities shared during our communication
			must be treated confidentially. Spreading or abusing personal data is strictly prohibited.</p></li>
		<li><p><b>Non-Violent Exchange:</b> Any form of violent or aggressive behavior is unacceptable. Threats,
			harassment, or any other form of violence will not be tolerated.</p></li>
		<li><p><b>Respecting Boundaries:</b> It is important to respect the personal boundaries of each individual. If
			someone clearly expresses discomfort with certain topics or situations, they should be respected.</p></li>
		<li><p><b>No Discrimination:</b> Every person, regardless of gender, skin color, origin, religion, sexual
			orientation, or other characteristics, deserves equal recognition and respect. Discrimination and hate
			speech will not be tolerated.</p></li>
		<li><p><b>Friendly Tone:</b> A polite and friendly tone should always be maintained. Swear words, derogatory
			remarks, or aggressive expressions are not allowed.</p></li>
		<li><p><b>Fairness and Empathy:</b> Fairness and empathy are the foundations of healthy communication. Treating
			each other with understanding and compassion promotes positive interactions.</p></li>
		<li><p><b>Responsible Actions:</b> Everyone is responsible for their own actions and statements. Spreading
			defamatory, false, or misleading information or intentionally causing harm is not allowed.</p></li>
		<li><p><b>Consequences for Violations:</b> In the event of a violation of these terms, I reserve the right to
			terminate contact or take other appropriate measures to ensure my safety and well-being.</p></li>
	</ol>
	<h2>Contact Termination</h2>
	<ul>
		<li><p>If you wish to block or terminate contact with me, you are required to provide a reason for your
			decision. This allows for open communication and ensures that misunderstandings can be addressed.</p></li>
		<li><p>Upon receiving your reason for the intended block, I will respond in a timely manner to provide a
			justifiable response or clarification, if applicable. It is important to note that you are not allowed to
			block or terminate contact before receiving my response. Waiting for my response ensures that I have the
			opportunity to address and justify your reason.</p></li>
		<li><p>Once you have made the decision to block or terminate contact, it cannot be reversed or undone. Blocking
			or terminating contact signifies the end of our communication and the closure of the relationship.</p></li>
		<li><p>It is encouraged that you attempt to resolve any issues or conflicts with me in advance, rather than
			announcing your intention to block without prior communication.</p></li>
	</ul>
